export const convertedLetter = (slug) => {
  if (slug === "steam") {
    return "Steam";
  } else if (slug === "playstation-store") {
    return "at Playstation Store";
  } else if (slug === "xbox-store") {
    return "at Xbox Store";
  } else if (slug === "nintendo") {
    return "at Nintendo";
  } else if (slug === "apple-appstore") {
    return "at App Store";
  } else if (slug === "gog") {
    return "at GOG";
  } else if (slug === "xbox360") {
    return "at Xbox 360 Store";
  } else if (slug === "google-play") {
    return "at Google Play";
  } else if (slug === "itch") {
    return "at itch.io";
  } else if (slug === "epic-games") {
    return "at Epic Games";
  } else if (slug === "pc") {
    return "for PC";
  } else if (slug === "playstation5") {
    return "for Playstation 5";
  } else if (slug === "xbox-one") {
    return "for Xbox One";
  } else if (slug === "playstation4") {
    return "for Playstation 4";
  } else if (slug === "xbox-series-x") {
    return "for Xbox Series S/X";
  } else if (slug === "nintendo-switch") {
    return "for Nintendo Switch";
  } else if (slug === "ios") {
    return "for iOS";
  } else if (slug === "android") {
    return "for Android";
  } else if (slug === "nintendo-3ds") {
    return "for Nintendo 3ds";
  } else if (slug === "") {
    return "for Android";
  } else if (slug === "android") {
    return "for Android";
  }
};

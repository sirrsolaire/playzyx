export const fullMonths = (getMonth) => {
  if (getMonth === "01") {
    return "January";
  } else if (getMonth === "02") {
    return "February";
  } else if (getMonth === "03") {
    return "March";
  } else if (getMonth === "04") {
    return "April";
  } else if (getMonth === "05") {
    return "May";
  } else if (getMonth === "06") {
    return "June";
  } else if (getMonth === "07") {
    return "July";
  } else if (getMonth === "08") {
    return "August";
  } else if (getMonth === "09") {
    return "September";
  } else if (getMonth === "10") {
    return "October";
  } else if (getMonth === "11") {
    return "November";
  } else if (getMonth === "12") {
    return "December";
  }
};

export const selectStore = (id) => {
  if (id === 3) {
    return "PlayStation Store";
  } else if (id === 5) {
    return "GOG";
  } else if (id === 1) {
    return "Steam";
  } else if (id === 2) {
    return "Xbox Store";
  } else if (id === 4) {
    return "App Store";
  } else if (id === 6) {
    return "Nintendo";
  } else if (id === 7) {
    return "Xbox 360 Store";
  } else if (id === 8) {
    return "Google Play";
  } else if (id === 9) {
    return "itch Store";
  } else if (id === 11) {
    return "Epic Store";
  }
};
